	<h1>Privacy Policy</h1>
	<h2>Last updated March 14, 2024</h2>
	<br />
	<p>
		This Privacy Policy applies to the PokeBook (the "App") and governs how it collects, uses, and
		discloses information collected from users ("you" or "your") of the app. The app is provided by
		Pokeghost (the "Developer"). This policy aims to be simple and straightforward, so you clearly
		understand everything that is important without unnecessary legal talk.
	</p>
	<br />
	<h2>PokeBook and your personal data</h2>
	<br />
	<p>
		PokeBook does not collect any personal or identifiable information. PokeBook allows you to write
		poems and notes that are processed and stored on your device by default. Thus, no special
		procedures are required.
	</p>
	<br />
	<h2>Sharing with third-parties</h2>
	<br />
	<p>
		PokeBook provides OPTIONAL integration capabilities with third-party cloud providers for storing
		poems and notes you write on their premises. When enabled, your poems and notes will be
		processed by them, and you will be subject to their terms of service and privacy policies. When
		choosing a third-party storage option, an access token, a special short-lived value, is stored
		locally on your device for interacting with the service. It is deleted when you log out. When
		connecting to a third-party service, PokeBook does not read your information, such as email,
		name, etc., and cannot read the contents of other files and folders on your cloud drive. You can
		find the full list of available storage providers in Settings.
	</p>
	<br />

	<h2>Your preferences and customizations</h2>
	<br />
	<p>
		PokeBook allows setting a number of preferences, such as font, color theme, text justification,
		etc. These preferences persist locally on your device and are never shared.
	</p>
	<br />
	<h2>Analytics and logs</h2>
	<br />
	<p>
		PokeBook does not use analytics services. PokeBook's server provider keeps short-lived logs of
		requests. These logs include the user agent. No other information related to you is included.
		These logs are collected only for the web version.
	</p>
	<br />
	<h2>Data retention and deletion</h2>
	<br />
	<p>
		Poems are stored for as long as you need them. Each poem can be deleted, and it is deleted
		immediately. Preferences are stored in your device's long-term storage. Preferences can be
		cleared by deleting the app or clearing the browser cache. No other data retention is done, so
		no specific data deletion procedures are necessary.
	</p>
	<br />
	<h2>Cookies</h2>
	<br />
	<p>PokeBook doesn't use cookies.</p>
	<br />
	<h2>Point of contact for inquires</h2>
	<br />
	<p>
		For any questions or clarifications regarding the privacy policy, please send an email to
		<u><b><a href="mailto:support@pokeghost.org">support@pokeghost.org</a></b></u>.
	</p>
</div>
