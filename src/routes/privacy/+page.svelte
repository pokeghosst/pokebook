	<h1>Privacy Policy</h1>
	<h2>Last updated February 7, 2024</h2>
	<br />
	<p>
		This Privacy Policy applies to the PokeBook (the "App") and governs how it collects, uses, and
		discloses information collected from users ("you" or "your") of the App. This policy aims to be
		as simple and straightforward as possible so you clearly understand everything that is important
		without unnecessary legal talk.
	</p>
	<br />
	<p>
		In short, PokeBook never requests any personal or identifiable information. If you log in with a
		cloud provider, your poems and personal data are stored and processed by the provider. Interface
		settings (font, alignment, theme, etc.) are stored locally. If you're using browser, they are
		stored in the `localStorage`. On mobile devices, settings are stored in `UserDefaults` (on iOS)
		or `SharedPreferences` (on Android). By default, your poems are stored locally. Regardless of
		your selected storage option, PokeBook never has access to the contents of your poems or your
		personal information. You can think of it as a text processing application.
	</p>
	<br />
	<p>
		PokeBook doesn't use any kind of analytics or session recording services (such as Goole
		Analytics, Facebook Pixel, Hotjar, etc.). PokeBook is deployed on Vercel platform that
		automatically keeps logs of requests. These logs include User Agent. No other information
		related to you is recorded.
	</p>
	<br />
	<h2>Where are poems stored?</h2>
	<br />
	<p>
		PokeBook provides three possible storage options for poems: browser's storage (if you're using
		it in browser), device file system (if you're using a mobile app from the store) and cloud
		storage provider. When using the former, poems and notes are stored in your local `IndexedDB`
		instance. When you're using a mobile app, poems are saved as regular text files on your device.
		PokeBook doesn't read into your filesystem and only saves files in the application persistent
		folder. Please note! If you uninstall the app, the poems will be gone. With these two options
		only those who have acces to your device can access your poems. On the other hand, when choosing
		cloud provider, poems and notes are stored there. This data is subject to provider's terms of
		service and privacy policy.
	</p>
	<br />
	<h2>What information is processed and how?</h2>
	<br />
	<p>
		When you use 'local' storage option, all information is stored and processed directly on your
		device (either in browser or in file system). No information about you or your device is
		collected.
	</p>
	<br />
	<p>
		When you use one of the integrated providers, your authentication data is processed by it.
		PokeBook has access only to files it created in your drive. PokeBook cannot access other files
		you have in your drive.
	</p>
	<br />
	<h2>Cookies</h2>
	<br />
	<p>PokeBook doesn't use cookies.</p>
